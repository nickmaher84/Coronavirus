select
    data                                   as date,
    stato                                  as country_code,
    codice_regione                         as region_code,
    denominazione_regione                  as region_name,
    lat                                    as latitude,
    long                                   as longitude,
    ricoverati_con_sintomi                 as recovering_with_symptoms,
    terapia_intensiva                      as in_intensive_care,
    totale_ospedalizzati                   as hospitalised,
    isolamento_domiciliare                 as isolating_at_home,
    totale_positivi                        as active_cases,
    variazione_totale_positivi             as change_in_active_cases,
    nuovi_positivi                         as new_cases,
    dimessi_guariti                        as total_recovered,
    deceduti                               as total_deaths,
    casi_da_sospetto_diagnostico           as total_cases_from_diagnosis,
    casi_da_screening                      as total_cases_from_screening,
    totale_casi                            as total_cases,
    tamponi                                as total_tests,
    casi_testati                           as total_cases_tested,
    note                                   as notes,
    ingressi_terapia_intensiva             as entering_intensive_care,
    note_test                              as notes_on_tests,
    note_casi                              as notes_on_cases,
    totale_positivi_test_molecolare        as total_cases_from_molecular_tests,
    totale_positivi_test_antigenico_rapido as total_cases_from_rapid_antigen_tests,
    tamponi_test_molecolare                as total_molecular_tests,
    tamponi_test_antigenico_rapido         as total_rapid_antigen_tests,
    codice_nuts_1                          as nuts1_code,
    codice_nuts_2                          as nuts2_code
from {{ source('pcm-dpc', 'regioni') }}
